const EXTENSIONS = ['webp', 'avif', 'png', 'jpg', 'jpeg', 'svg']

function resolveAsset(path) {
  if (!path) return null
  if (VISUAL_ASSET_PATHS.has(path)) return path

  const base = path.replace(/\.(webp|avif|png|jpe?g|svg)$/i, '')
  for (const ext of EXTENSIONS) {
    const candidate = `${base}.${ext}`
    if (VISUAL_ASSET_PATHS.has(candidate)) return candidate
  }
  return null
}

function palette(seed = '') {
  let hash = 0
  for (const char of seed) hash = (hash * 31 + char.charCodeAt(0)) >>> 0
  return PALETTES[hash % PALETTES.length]
}

function makeVisual({
  slug,
  title,
  type,
  kanji,
  fallbackImage,
  expectedPath,
  image = null,
  // Imagen vertical opcional (1024x1536 portrait 2:3) usada por
  // VisualPageShell para el fondo de página completa. Se separa del
  // `image` (horizontal 16:9 para CinematicHero/banners) porque una
  // imagen 16:9 estirada para cubrir una sección de página de 200vh+
  // se ve recortada o estirada feo. Si shellImage no se define, el
  // shell no muestra imagen de fondo — solo overlay gradient + color.
  shellExpectedPath = null,
  shellImage = null,
  shellObjectPosition = 'center top',
  // Opacidad del shellImage en el fondo de página completa. Default
  // 0.75 funciona bien para composiciones balanceadas. Para imágenes
  // visualmente densas (mucho detalle pequeño, haces de luz intensos,
  // mucha saturación) bajar a 0.30-0.45 para que respiren.
  shellOpacity = 0.75,
  description = '',
  mood = '',
  objectPosition = 'center',
  paletteSeed = slug,
  accent,
  accentRgb,
  glow,
  glowRgb,
  atmosphere,
}) {
  const p = palette(paletteSeed)
  const resolvedImage = image ?? resolveAsset(expectedPath) ?? fallbackImage
  const resolvedShellImage =
    shellImage ?? (shellExpectedPath ? resolveAsset(shellExpectedPath) : null)
  return {
    slug,
    title,
    type,
    kanji,
    image: resolvedImage,
    shellImage: resolvedShellImage,
    shellObjectPosition,
    shellOpacity,
    fallbackImage,
    expectedPath,
    shellExpectedPath,
    description,
    mood,
    objectPosition,
    accent: accent ?? p.accent,
    accentRgb: accentRgb ?? p.accentRgb,
    glow: glow ?? p.glow,
    glowRgb: glowRgb ?? p.glowRgb,
    atmosphere,
  }
}

